#include <string.h>

size_t		strspllen(char const *s, char c)
{
	size_t		len;

	len = 0;
	while (*s)
	{
		if (*s != c)
			len++;
		while (*s != c && *s != '\0')
			s++;
		while (*s == c)
			s++;
	}
	
	return (len);
}
